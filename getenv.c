#include "shell.h"

/**
 * get_environ - return string array copy of our environ
 * @info: Structure containing args.
 * Return: Always 0
 */
char **get_environ(info_t *info)
{
	if (!info->environ || info->env_changed)
	{
		info->environ = list_to_strings(info->env);
		info->env_changed = 0;
	}

	return (info->environ);
}

/**
 * unsetenv_fun - Remove an env
 * @info: Structure containing args.
 * @var: the string env var property
 * Return: 1.
 */
int unsetenv_fun(info_t *info, char *var)
{
	list_t *node = info->env;
	size_t i = 0;
	char *p;

	if (!node || !var)
		return (0);

	while (node)
	{
		p = starts_with(node->str, var);
		if (p && *p == '=')
		{
			info->env_changed = delete_node_at_index(&(info->env), i);
			i = 0;
			node = info->env;
			continue;
		}
		node = node->next;
		i++;
	}
	return (info->env_changed);
}

/**
 * setenv_fun - Init new env var.
 * @info: Structure containing args.
 * @var: string env var property
 * @value: string env var value
 *  Return: Always 0
 */
int setenv_fun(info_t *info, char *var, char *value)
{
	char *buf = NULL;
	list_t *node;
	char *p;

	if (!var || !value)
		return (0);

	buf = malloc(strlen_fun(var) + strlen_fun(value) + 2);
	if (!buf)
		return (1);
	strcpy_fun(buf, var);
	strcat_fun(buf, "=");
	strcat_fun(buf, value);
	node = info->env;
	while (node)
	{
		p = starts_with(node->str, var);
		if (p && *p == '=')
		{
			free(node->str);
			node->str = buf;
			info->env_changed = 1;
			return (0);
		}
		node = node->next;
	}
	add_node_end(&(info->env), buf, 0);
	free(buf);
	info->env_changed = 1;
	return (0);
}
